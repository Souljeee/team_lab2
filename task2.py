def bubble_sort(arr):
    n = len(arr)

    for i in range(n):
        # Флаг, который указывает, были ли какие-либо обмены на этой итерации
        swapped = False

        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                # Меняем элементы, если текущий больше следующего
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True

        # Если на этой итерации не было обменов, то массив уже отсортирован
        if not swapped:
            break

# Пример списка для сортировки
my_list = [64, 34, 25, 12, 22, 11, 90]

# Вызываем функцию сортировки
bubble_sort(my_list)

# Выводим отсортированный список в консоль
print("Отсортированный список:", my_list)
